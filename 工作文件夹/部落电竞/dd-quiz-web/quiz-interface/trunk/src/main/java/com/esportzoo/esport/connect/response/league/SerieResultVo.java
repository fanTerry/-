package com.esportzoo.esport.connect.response.league;

import java.io.Serializable;
import java.util.Date;
import java.util.List;

/**
 * @author tingjun.wang
 * @date 2019/8/20 14:34
 */
public class SerieResultVo implements Serializable {
	private static final long serialVersionUID = -9015155756602022287L;
	private Long id;
	private Long serieId;
	private String name;
	/** 全称 */
	private String fullName;
	/** 年份 */
	private Integer year;
	/** 开始时间 */
	private Date beginAt;
	/** 结束时间 */
	private Date endAt;
	private List<TournamentResultVo> tournamentResultVoList;
	private List<TeamResultVo> teamResultVoList;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getSerieId() {
		return serieId;
	}

	public void setSerieId(Long serieId) {
		this.serieId = serieId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getFullName() {
		return fullName;
	}

	public void setFullName(String fullName) {
		this.fullName = fullName;
	}

	public Integer getYear() {
		return year;
	}

	public void setYear(Integer year) {
		this.year = year;
	}

	public Date getBeginAt() {
		return beginAt;
	}

	public void setBeginAt(Date beginAt) {
		this.beginAt = beginAt;
	}

	public Date getEndAt() {
		return endAt;
	}

	public void setEndAt(Date endAt) {
		this.endAt = endAt;
	}

	public List<TournamentResultVo> getTournamentResultVoList() {
		return tournamentResultVoList;
	}

	public void setTournamentResultVoList(List<TournamentResultVo> tournamentResultVoList) {
		this.tournamentResultVoList = tournamentResultVoList;
	}

	public List<TeamResultVo> getTeamResultVoList() {
		return teamResultVoList;
	}

	public void setTeamResultVoList(List<TeamResultVo> teamResultVoList) {
		this.teamResultVoList = teamResultVoList;
	}
}
