package com.esportzoo.esport.vo.ubox;

import java.io.Serializable;

/**
 * @author tingting.shen
 * @date 2019/06/10
 */
public class UboxUserInfoResponse implements Serializable{

	private static final long serialVersionUID = 6441563636007279739L;
	
	private String errcode;
	private String errmsg;
	private UboxUserInfo data;
	
	public String getErrcode() {
		return errcode;
	}
	public String getErrmsg() {
		return errmsg;
	}
	public UboxUserInfo getData() {
		return data;
	}
	public void setErrcode(String errcode) {
		this.errcode = errcode;
	}
	public void setErrmsg(String errmsg) {
		this.errmsg = errmsg;
	}
	public void setData(UboxUserInfo data) {
		this.data = data;
	}

}
