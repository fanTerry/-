package com.esportzoo.esport.connect.response.payment;

import java.io.Serializable;
import java.math.BigDecimal;

/**
 * @description: 充值金额列表
 *
 * @author: Haitao.Li
 *
 * @create: 2019-10-17 17:07
 **/
public class ChargeMoneyResponse  implements Serializable {


	private static final long serialVersionUID = -1077517356299639394L;
	private BigDecimal money;
	private Long starNum;
	private Long sendStar ;

	public BigDecimal getMoney() {
		return money;
	}

	public void setMoney(BigDecimal money) {
		this.money = money;
	}

	public Long getStarNum() {
		return starNum;
	}

	public void setStarNum(Long starNum) {
		this.starNum = starNum;
	}

	public Long getSendStar() {
		return sendStar;
	}

	public void setSendStar(Long sendStar) {
		this.sendStar = sendStar;
	}
}
