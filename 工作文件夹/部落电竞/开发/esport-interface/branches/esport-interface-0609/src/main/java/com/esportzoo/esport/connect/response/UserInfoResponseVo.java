package com.esportzoo.esport.connect.response;

import java.io.Serializable;

/**
 * @description: 前端展示用户信息
 *
 * @author: Haitao.Li
 *
 * @create: 2019-11-26 10:09
 **/
public class UserInfoResponseVo implements Serializable {

	private static final long serialVersionUID = -8485522205532288314L;
	/** 用户昵称 **/
	private String nickName;

	/** 头像 **/
	private String icon;

	/**用户id*/
	private Long userId;

	/** 联系电话 **/
	private String phone;

	public String getNickName() {
		return nickName;
	}

	public void setNickName(String nickName) {
		this.nickName = nickName;
	}

	public String getIcon() {
		return icon;
	}

	public void setIcon(String icon) {
		this.icon = icon;
	}

	public Long getUserId() {
		return userId;
	}

	public void setUserId(Long userId) {
		this.userId = userId;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}
}
