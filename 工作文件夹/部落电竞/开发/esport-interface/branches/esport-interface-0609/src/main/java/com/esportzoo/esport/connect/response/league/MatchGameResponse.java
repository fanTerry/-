package com.esportzoo.esport.connect.response.league;

import java.io.Serializable;

public class MatchGameResponse implements Serializable {
	private static final long serialVersionUID = 9169245190399495011L;

	private  Integer videogameId;

	private String videoGameName ;

	private Integer currLiveMatchNum ;

	private boolean selected ;

	public Integer getVideogameId() {
		return videogameId;
	}

	public void setVideogameId(Integer videogameId) {
		this.videogameId = videogameId;
	}

	public String getVideoGameName() {
		return videoGameName;
	}

	public void setVideoGameName(String videoGameName) {
		this.videoGameName = videoGameName;
	}

	public Integer getCurrLiveMatchNum() {
		return currLiveMatchNum;
	}

	public void setCurrLiveMatchNum(Integer currLiveMatchNum) {
		this.currLiveMatchNum = currLiveMatchNum;
	}

	public boolean isSelected() {
		return selected;
	}

	public void setSelected(boolean selected) {
		this.selected = selected;
	}
}
