package com.esportzoo.esport.connect.response.league;

import com.esportzoo.leaguelib.common.result.MatchResult;

import java.io.Serializable;
import java.util.Date;
import java.util.List;

public class MatchResponse implements Serializable {

	private static final long serialVersionUID = -1742570660158555319L;

	private String sortMatchDate;

	private String videoGameName;

	private String legueName;

	private Integer gameType;

	private Integer upSum;
	private Date sortDate;

	private List<MatchResult> matchResultList;

	public String getSortMatchDate() {
		return sortMatchDate;
	}

	public void setSortMatchDate(String sortMatchDate) {
		this.sortMatchDate = sortMatchDate;
	}

	public String getVideoGameName() {
		return videoGameName;
	}

	public void setVideoGameName(String videoGameName) {
		this.videoGameName = videoGameName;
	}

	public String getLegueName() {
		return legueName;
	}

	public void setLegueName(String legueName) {
		this.legueName = legueName;
	}

	public List<MatchResult> getMatchResultList() {
		return matchResultList;
	}

	public void setMatchResultList(List<MatchResult> matchResultList) {
		this.matchResultList = matchResultList;
	}

	public Integer getGameType() {
		return gameType;
	}

	public void setGameType(Integer gameType) {
		this.gameType = gameType;
	}

	public Integer getUpSum() {
		return upSum;
	}

	public void setUpSum(Integer upSum) {
		this.upSum = upSum;
	}

	public Date getSortDate() {
		return sortDate;
	}

	public void setSortDate(Date sortDate) {
		this.sortDate = sortDate;
	}
}
