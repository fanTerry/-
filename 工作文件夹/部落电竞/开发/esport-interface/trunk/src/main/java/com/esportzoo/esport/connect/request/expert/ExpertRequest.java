package com.esportzoo.esport.connect.request.expert;

import com.esportzoo.esport.connect.request.BasePageRequest;

/**
 * @description: 专家首页
 *
 * @author: Haitao.Li
 *
 * @create: 2019-05-10 10:30
 **/
public class ExpertRequest extends BasePageRequest {

	private String matchId ;
	private Long userId;
	private  Integer clientType;

	/**
	 * 专家申请信息
	 */
	/** 运营者身份证正面照地址 */
	private String frontImage;

	/** 运营者身份证背面照地址 */
	private String backImage;

	private String expertName;
	private String introduction;
	private String reason;
	private String realName;
	private String cardNo;
	private String expertIcon;

	public String getMatchId() {
		return matchId;
	}

	public void setMatchId(String matchId) {
		this.matchId = matchId;
	}

	public Long getUserId() {
		return userId;
	}

	public void setUserId(Long userId) {
		this.userId = userId;
	}

	public String getExpertName() {
		return expertName;
	}

	public void setExpertName(String expertName) {
		this.expertName = expertName;
	}

	public String getIntroduction() {
		return introduction;
	}

	public void setIntroduction(String introduction) {
		this.introduction = introduction;
	}

	public String getReason() {
		return reason;
	}

	public void setReason(String reason) {
		this.reason = reason;
	}

	public String getRealName() {
		return realName;
	}

	public void setRealName(String realName) {
		this.realName = realName;
	}

	public String getCardNo() {
		return cardNo;
	}

	public void setCardNo(String cardNo) {
		this.cardNo = cardNo;
	}

	public String getFrontImage() {
		return frontImage;
	}

	public void setFrontImage(String frontImage) {
		this.frontImage = frontImage;
	}

	public String getBackImage() {
		return backImage;
	}

	public void setBackImage(String backImage) {
		this.backImage = backImage;
	}

	public String getExpertIcon() {
		return expertIcon;
	}

	public void setExpertIcon(String expertIcon) {
		this.expertIcon = expertIcon;
	}

	public Integer getClientType() {
		return clientType;
	}

	public void setClientType(Integer clientType) {
		this.clientType = clientType;
	}
}
